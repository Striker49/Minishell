#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		size;
	char	*dup;

	size = 0;
	while (src[size])
		size++;
	dup = malloc(sizeof(char) * (size + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
